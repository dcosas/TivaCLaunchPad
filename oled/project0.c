void oledInit(void)
{
	/*
	 * SCL - Clock - PA2
	 * SDA - MOSI  - PA5
	 * RES - RESET - PA7
	 * DC  - DC    - PA6
	 */
	uint8_t vccstate = 0;
	SSI_init();
	GPIOPinWrite(GPIO_PORTA_BASE, GPIO_PIN_7, GPIO_PIN_7);
	GPIOPinWrite(GPIO_PORTA_BASE, GPIO_PIN_3, 0);
	GPIOPinWrite(GPIO_PORTA_BASE, GPIO_PIN_6, 0);
	SysCtlDelay(30000);
	GPIOPinWrite(GPIO_PORTA_BASE, GPIO_PIN_7, 0);
	SysCtlDelay(100000);
	GPIOPinWrite(GPIO_PORTA_BASE, GPIO_PIN_7, GPIO_PIN_7);
	SysCtlDelay(100000);
	oledWrite(CMD_LCD,0xAE);
	oledWrite(CMD_LCD,0xD5);
	oledWrite(CMD_LCD,0x80);
	oledWrite(CMD_LCD,0xA8);
	oledWrite(CMD_LCD,0x3F);
	oledWrite(CMD_LCD,0xD3);
	oledWrite(CMD_LCD,0x0);
	oledWrite(CMD_LCD,0x40);
	oledWrite(CMD_LCD,0x8D);
	if(vccstate)
	{
		oledWrite(CMD_LCD,0x10);//externalvcc
	}
	else
	{
		oledWrite(CMD_LCD,0x14);//externalvcc
	}
	oledWrite(CMD_LCD,0x20);
	oledWrite(CMD_LCD,0x00);
	oledWrite(CMD_LCD,0xA1);
	oledWrite(CMD_LCD,0xC8);//COMSCANDEC
	oledWrite(CMD_LCD,0xDA);
	oledWrite(CMD_LCD,0x12);
	oledWrite(CMD_LCD,0x81);
	if(vccstate)
	{
		oledWrite(CMD_LCD,0x9F);//externalvcc
	}
	else
	{
		oledWrite(CMD_LCD,0xCF);//externalvcc
	}

	oledWrite(CMD_LCD,0xd9);
	oledWrite(CMD_LCD,0x22);
	oledWrite(CMD_LCD,0xDB);
	oledWrite(CMD_LCD,0x40);
	oledWrite(CMD_LCD,0xA4);
	oledWrite(CMD_LCD,0xA6);
	oledWrite(CMD_LCD,0xAF);
	SysCtlDelay(10000);
}



